package com.bms.system.extapp.bean;;

public class Table {

	private String colnumName;

    private String colnumType;

    private String isNull;

    private String key;

    private String defaultValue;

    private String extra;
    
    private String attrName;
    
    private String attrType;
    
    private int length;

	public String getColnumName() {
		return colnumName;
	}

	public void setColnumName(String colnumName) {
		this.colnumName = colnumName;
	}

	public String getColnumType() {
		return colnumType;
	}

	public void setColnumType(String colnumType) {
		this.colnumType = colnumType;
	}

	public String getIsNull() {
		return isNull;
	}

	public void setIsNull(String isNull) {
		this.isNull = isNull;
	}

	public String getKey() {
		return key;
	}

	public void setKey(String key) {
		this.key = key;
	}

	public String getDefaultValue() {
		return defaultValue;
	}

	public void setDefaultValue(String defaultValue) {
		this.defaultValue = defaultValue;
	}

	public String getExtra() {
		return extra;
	}

	public void setExtra(String extra) {
		this.extra = extra;
	}

	public String getAttrName() {
		return attrName;
	}

	public void setAttrName(String attrName) {
		this.attrName = attrName;
	}

	public String getAttrType() {
		return attrType;
	}

	public void setAttrType(String attrType) {
		this.attrType = attrType;
	}

	public int getLength() {
		return length;
	}

	public void setLength(int length) {
		this.length = length;
	}
    
    
}
